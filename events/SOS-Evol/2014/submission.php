<h1>Submission</h1>

<p>
	SOS-Evol encourages PhD students, researchers, open source developers and industry practitioners to submit an extended abstract or full paper presenting their vision, work or experience in the domain of evolving open source software. Topics of interest include, but are not limited to:
	
	<ul>
		<li><strong>Tools, methods and techniques</strong> to support the evolution of open source software projects.</li>
		<li><strong>Models</strong> to show and predict the evolution of open source software.</li>				
		<li><strong>Industrial experience</strong> on problems, successes and failures related to open source software adoption and its evolution.</li>
		<li>Evolution of <strong>hybrid</strong> open/closed source projects.</li>
		<li><strong>Challenges and pitfalls</strong> raised by the study or the support of open source software evolution.</li>
		<li><strong>Early results</strong> and <strong>visionary statement</strong> related to open source software evolution.</li>
		<li><strong>Empirical studies</strong> of the evolution of open source software systems.</li>
		<li><strong>Availability</strong> and <strong>accessibility</strong> of open source development data.</li>
		<li><strong>New kinds</strong> of open source evolution: mobile application, cloud systems, &hellip;</li>
	</ul>	
</p>

<p>Two types of workshop submissions are solicited:
	<ol>
		<li>Position paper of 1 to 3 pages (A4 format, Times font, 10pt size) are solicited from open source developers or industry practitioners to point out important challenges and practical problems in the evolution of open source projects or to suggest research topics to the community. These submissions will be reviewed for workshop presentation only, they will not be part of the formal workshop proceedings.</li>
		<li>Full research papers of 12 to 15 pages in PDF format (following the <a href="http://journal.ub.tu-berlin.de/eceasst">ECEASST</a> <a href="ECEASST-cls-SOSEVOL.zip">LaTeX template</a>). They should present original ideas or results in the area of the evolution of open source software. Depending on the quality and contents of these papers, they will be considered for inclusion in the the formal online workshop proceedings to be published in the Electronic Communications of the EASST.</li>	
	</ol>
</p>

<p>Contributions need to be submitted online via <a href="http://www.easychair.org/conferences/?conf=sosevol2014">the EasyChair conference system</a>. All submitted papers and abstracts will be reviewed by at least three members of the workshop Program Committee, at least one of which will be an open source contributor. Accepted submissions must be presented during SOS-Evol 2014 by one of the registered authors.</p>

