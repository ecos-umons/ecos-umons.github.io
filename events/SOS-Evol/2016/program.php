			<td>16h: <a href="http://informatique.umons.ac.be/staff/Claes.Maelick/">Maelick Claes</a> (University of Mons, Belgium) </td>
			<td>Maintainability Issues in Open Source Component-based Software Ecosystems</td>
			<td><b>public PhD defense</b>: Open source software ecosystems consist of a large number of
			interconnected software components. Component management software keeps track
			of the relations between these components, and helps both users and developers
			dealing with component dependencies and conflicts. Nevertheless, maintainability
			issues, such as backward-incompatible component updates, remain inevitable.
			In my thesis I aim to understand how dependency relations between software
			components maintained by different people impact software maintainability issues.
			By understanding how constraints between components can detect, prevent or
			reduce these issues, tools can be built to support maintainer and user
			communities of component-based ecosystems and distributions. All of this is
			investigated using the Debian and R ecosystem as a case study.</td>
