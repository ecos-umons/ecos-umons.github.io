<h1 class="titre">SOS-EVOL 2016 Seminar on Open Source Evolution</h1>
 
<p><strong>SOS-Evol</strong> is a series of annual international seminars gathering
PhD students, researchers and practitioners interested in studying the evolution of
open source software.
</p>

