<p>
Inasmuch as Mons is a small city, the hotels available near to the symposium location are very few and relatively small. So if you intend to stay at Mons for the night, we suggest you to quickly book a room.	
</p>

<p>A limited number of rooms have been reserved for BENEVOL 2013 participants in Hotel Infotel. Room price is between 80 and 100 euros. If you want to book a room, please do so before 15 november 2013 by sending a mail to <a href="mailto:info@hotelinfotel.be">info@hotelinfotel.be</a> specifying your name, number of room occupants, duration of stay, and reservation number G21315</p>

