<h1 id="introduction" style="border-top: none; padding-top: 0;">Welcome to the homepage of BENEVOL 2013</h1>
  
<p>
The 12th edition of the BElgian-NEtherlands software eVOLution seminar (BENEVOL 2013) will take place in Mons, Belgium on the 16th and 17th of December.
</p>

<p>The goal of BENEVOL is to bring together researchers from Belgium, The Netherlands and the neighboring countries that are working in the field of software evolution. BENEVOL offers an informal forum to meet and to discuss new ideas, important problems and obtained research results. Prospective speakers are invited to submit an extended abstract (max. 2 A4 pages) of a talk; based on these extended abstracts, the organization will construct a program.
</p>

<p>Although BENEVOL is a free event, places are limited, so registration is mandatory. You can register via <a href="https://docs.google.com/forms/d/1aWvetWvkwx3siFgShaSs576_xfmUQs4APqFyKOFRCmI/viewform">the dedicated form</a>.</p>

<p>The workshop will be organized by <a href="http://w3.umons.ac.be/staff/Mens.Tom/">Tom Mens</a>, <a href="http://w3.umons.ac.be/staff/Claes.Maelick/">Maëlick Claes</a>, and <a href="http://w3.umons.ac.be/staff/Goeminne.Mathieu/">Mathieu Goeminne</a>.</p>

