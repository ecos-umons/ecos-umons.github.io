<h2>Getting to Mons</h2>

<ul>
	<li><strong>By plane</strong>: 
		<p>
			Fly to <a href="http://www.charleroi-airport.com/en/brussels-south-charleroi-airport/index.html">Brussels South Charleroi Airport</a>.
		</p>
		<p>At the airport, go to one of the ticket dispensers near Door 2 and take a <strong>combined ticket bus/train</strong> to Mons.</p>
		<p>Bus connections are organised according to flights arrivals and departures (every half hour to Charleroi station and return). Take the bus to Charleroi station, from where you can continue by train to Mons.</p>
	</li>
	<li><strong>By train</strong>:
		<p>From any Belgian station, take the train towards Mons. From Brussels, there is a train every half hour. The new train station in Mons is under construction, but a temporary station is in service close to the future one.</p>
		<p>Timetables are available on <a href="http://www.belgianrail.be/en/Default.aspx">the web site of the SNCB</a>, or by phone (+32/2/555.25.25).</p>
		<p>
		<p>At your arrival, cross the square and take the street on your left (see the map).</p>
	</li>
</ul>

